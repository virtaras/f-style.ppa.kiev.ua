						<h2>История марки</h2>

						<p>Основательница марки Селин Випиана в 1945 году вместе со своим мужем открыла в Париже бутик, где продавалась детская обувь. Отличное качество товара и хорошие отзывы покупателей натолкнули супругов на мысль о расширении производства: ассортимент пополнился мужскими и женскими моделями обуви, также под маркой Celine стали выпускаться аксессуары из кожи. Сумки, обувь и одежду Celine изготавливает из кожи пони, что становится отличительной чертой марки.</p>

						<p>В конце 1950-х наибольшим спросом пользовались мокасины Céline, украшенные “лошадиными поводьями”, и сумки из кожи пони. В 1964 году вышел первый парфюм от Céline “Vent Fou” (“Безумный ветер”). Вслед за этим были представлены первая коллекция женской одежды prêt-à-porter, названная “Couture Sportswear”, и новая форма мокасинов – с каблуком, получившая название “Inca”.<br/>
						В шестидесятых годах ассортимент марки уже вплотную приближается к современному – Céline становится известным брендом одежды и аксессуаров для женщин. В 1970-х бутики Céline открылись по всему миру – в Монте-Карло, Риме, Лозанне, Женеве, Гонконге и Торонто.</p>
